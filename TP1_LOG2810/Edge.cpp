#include "Edge.h"


////////////////////////////////////////////////////////////////////////
///
/// @fn Edge::Edge (int distance) : distance_ ( distance )
///
/// Ne fait qu'initialiser les menbre
///
/// @return Aucune (constructeur).
///
////////////////////////////////////////////////////////////////////////
Edge::Edge (int distance) : distance_ ( distance ) {}


////////////////////////////////////////////////////////////////////////
///
/// @fn Edge::~Edge () {} 
/// 
/// destructeur
///
/// @return Aucune (constructeur).
///
////////////////////////////////////////////////////////////////////////
Edge::~Edge () {}

///////////////////////////////////////////////////////////////////////
///
/// @fn void Edge::setDistance ( int distance )
/// 
/// assigne le poid de l'arc
///
/// @return Aucune
///
////////////////////////////////////////////////////////////////////////
void Edge::setDistance ( int distance ) {

	distance_ = distance;
}


///////////////////////////////////////////////////////////////////////
///
/// @fn int Edge::getDistance ()
/// 
/// retourne le poid de l'arc
///
/// @return Aucune
///
////////////////////////////////////////////////////////////////////////
int Edge::getDistance () {

	return distance_;
}
